class RelationalDemo{

	public static void main(String[] ar){

		int a=10;
		int b=20;
		boolean ans;

		ans= a==b;
		System.out.println(ans);//false

		ans= a!=b;
		System.out.println(ans);//true

		ans= a<b;
		System.out.println(ans);//true

		ans= a>b;
		System.out.println(ans);//False

		ans= a<=b;
		System.out.println(ans);//true

		ans= a>=b;
		System.out.println(ans);//false



	}
}
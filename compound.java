//Change the following program to use compound assignments:


class Compound{

	public static void main(String[] args) {
		
		int result=3;
		System.out.println(result);

		result-= 2;
		System.out.println(result);

		result*=8;
		System.out.println(result);

		result /=2;
		System.out.println(result);

		result %=2;
		System.out.println(result);
	}
}